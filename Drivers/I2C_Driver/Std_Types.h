#ifndef STD_TYPES_H_
#define STD_TYPES_H_

/*-----for unsigned types-----*/
typedef unsigned char  u8;
typedef unsigned short u16;
typedef unsigned long  u32;

/*-----for signed types-----*/
typedef signed char  s8;
typedef signed short s16;
typedef signed long  s32;

/*-----for floar and double-----*/
typedef float  f32;
typedef double f64;

#endif /* STD_TYPES_H_ */
